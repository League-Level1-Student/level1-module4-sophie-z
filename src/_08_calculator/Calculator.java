package _08_calculator;

public class Calculator {
int add(int anumber1, int anumber2) {
	return anumber1+anumber2;
}

int multiply(int mnumber1, int mnumber2){
	return mnumber1*mnumber2;
}

int subtract(int snumber1, int snumber2) {
	return snumber1-snumber2;
}

int divide(int dnumber1, int dnumber2) {
	return  dnumber1/dnumber2;
}

}
